    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        // Initialize the CategoryDAO
        CategoryDAO cDAO = new CategoryDAO();
        List<Category> list;

        // Retrieve the search parameter from the request
        String search = request.getParameter("search");

        if (search == null || search.trim().isEmpty()) {
            // No search term provided, fetch all categories
            list = cDAO.getAllCategory();
        } else {
            // Search term provided, fetch categories filtered by the search term
            list = cDAO.getCategoriesByName(search.trim());
        }

        // Set the fetched categories as a request attribute
        request.setAttribute("cate", list);

        // Forward the request to the JSP page for rendering
        request.getRequestDispatcher("ListCategory.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("ListCategory.jsp").forward(request, response);
    }

